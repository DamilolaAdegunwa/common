        public static string CreateBranchRegionJson()
        {
            var branch = Zenith.ZENITHBANK_BRANCHES;
            var region = Zenith.ZENITHBANK_REGION_CORRESPONDINGTOABOVEBRANCHES;

            var bArr = branch.Split('\r')?.Select(s => s?.Trim('\r')?.Trim()?.Replace("'", "''"))?.ToArray();
            var rArr = region.Split('\r')?.Select(s => s?.Trim('\r')?.Trim()?.Replace("'", "''"))?.ToArray();

            if(bArr.Length != rArr.Length)
            {
                throw new Exception();
            }

            List<BranchRegion> branchRegions = new List<BranchRegion>();

            for(int i = 0; i < bArr.Length; i++)
            {
                var br = new BranchRegion
                {
                    Branch = bArr[i],
                    Region = rArr[i]
                };
                branchRegions.Add(br);
            }

            var branchRegionsJson = JsonConvert.SerializeObject(branchRegions);

            return branchRegionsJson;
        }

