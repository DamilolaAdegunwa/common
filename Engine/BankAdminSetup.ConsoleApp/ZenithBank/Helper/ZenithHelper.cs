                    temp = temp.Replace("{{branchname}}", branchRow);
                    temp = temp.Replace("{{branchcode}}", null);
                    temp = temp.Replace("{{regionname}}", correspondingZonesRow);

                    branchSqlString += temp;
                }
                catch (Exception ex)
